

//.  inicio.    condição.   incremento.
Console.WriteLine("Subindo:");
for (int t = 0; t < 3; t++)
{
    Console.WriteLine($"Valor do t: {t}");
}


Console.WriteLine("Descendo:");
for (int t = 3; t > 0; t--)
{
    Console.WriteLine($"Valor do t: {t}");
}




